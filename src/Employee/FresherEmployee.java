package Employee;

import java.util.Date;

public class FresherEmployee extends Employee {
    private Date graduationDate;

    private String educationRank;

    private String education;

    public Date getGraduationDate() {
        return graduationDate;
    }

    public void setGraduationDate(Date graduationDate) {
        this.graduationDate = graduationDate;
    }

    public String getEducationRank() {
        return educationRank;
    }

    public void setEducationRank(String educationRank) {
        this.educationRank = educationRank;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    

    
    
}
